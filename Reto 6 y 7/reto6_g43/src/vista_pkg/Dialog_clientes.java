    void borrar_cliente() {
        int fila = tb_pet_owners.getSelectedRow();
        int id = Integer.parseInt(txt_idM.getText());
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "No has seleccionado un cliente/dueño");
        } else {

            System.out.println("ID: " + id);
            String query = "DELETE FROM tb_pet_owners WHERE id = " + id;
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El dueño ha sido eliminado con exito");
                clear_rows_table();
                show_pet_owners();
            } catch (HeadlessException | SQLException e) {
            }
        }
    }

    void clear_rows_table() {
        for (int i = 0; i < tb_pet_owners.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
        txt_idM.setText("");
        txt_nombreM.setText("");
        txt_docM.setText("");
        txt_telefonoM.setText("");
        
    }

    

