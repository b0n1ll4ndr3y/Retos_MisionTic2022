            int contact = Integer.parseInt((String)tb_pet_owners.getValueAt(row, 4).toString());
            String gender = (String)tb_pet_owners.getValueAt(row, 5);
            System.out.println(id + " - " + owner_name + " - " + document_type + " - " + document + " - " + contact + " - " + gender);
