    void show_pet_owners() {
        String sql = "SELECT * FROM tb_pet_owners";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            //Los datos que devuelve la consulta se muestran en la tabla
            Object[]pet_owners = new Object[6];
            modelo = (DefaultTableModel) tb_pet_owners.getModel();
            while (rs.next()) {
                pet_owners[0] = rs.getInt("id");
                pet_owners[1] = rs.getString("owner");
                pet_owners[2] = rs.getString("document_type");
                pet_owners[3] = rs.getInt("document");
                pet_owners[4] = rs.getInt("contact");
                pet_owners[5] = rs.getString("gender");
                modelo.addRow(pet_owners);
                System.out.println(rs.getInt("id"));
            }
            tb_pet_owners.setModel(modelo);
        } catch (SQLException e) {

        }
    }
    
    void anadir_cliente() {
        String name_owner = txt_nombreM.getText();
        String document_type = cb_tipo_doc.getSelectedItem().toString();
        int document = Integer.parseInt(txt_docM.getText());
        int contact = Integer.parseInt(txt_telefonoM.getText());
        String gender = "";
        if(op_m.isSelected()){
            gender = "Masculino";
        }else{
            gender = "Femenino";
        }

        if (name_owner.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del duaño");
        } else {
            String query = "INSERT INTO `tb_pet_owners`(`owner`, `document_type`, `document`, `contact`, `gender`) VALUES('" + name_owner + "', '" + document_type
                    + "', '" + document + "', '" + contact + "', '" + gender + "')";

            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El cliente/dueño ha sido creado");
                 clear_rows_table();
                show_pet_owners();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "No se pudo crear el dueño");
            }
        }
    }

